function tellFortune(numChildren, partnerName, location, jobTitle) {
    console.log(`You will be a ${jobTitle} in ${location}, and married to ${partnerName} with ${numChildren} kids.`);
}

// Calling the function three times with different values
tellFortune(3, 'Alice', 'New York', 'Software Engineer');
tellFortune(2, 'Bob', 'San Francisco', 'Data Scientist');
tellFortune(4, 'Charlie', 'Austin', 'Product Manager');
